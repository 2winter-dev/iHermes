export function resolveLanguage(preference: LanguagePreference, locale: string): SupportedLanguage {
  if (preference === 'zh' || preference === 'en') {
    return preference;
  }
  return locale.toLowerCase().startsWith('zh') ? 'zh' : 'en';
}
